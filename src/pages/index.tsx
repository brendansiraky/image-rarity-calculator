const inter = Inter({ subsets: ['latin'] })

async function fetcher() {
	try {
		const response = await fetch('/api/hello')
		const result = await response.json()
		return JSON.parse(result)
	} catch (err) {
		console.error(err)
	}
}

export default function Home() {
	const [data, setData] = useState<any>([])
	const [error, setError] = useState<boolean | string>(false)

	useEffect(() => {
		(async () => {
			const data = await fetcher()

			let batched = {
				background: {
					totalRarity: 0,
					traits: []
				},
				base: {
					totalRarity: 0,
					traits: []
				},
				eyes: {
					totalRarity: 0,
					traits: []
				},
				body: {
					totalRarity: 0,
					traits: []
				},
				head: {
					totalRarity: 0,
					traits: []
				},
			} as any
			
			for (let i = 0; i < data.length; i++) {
		
				const [traitType, value] = Object.entries(data[i])[0] as [string, string[]]

				value.forEach((traitName: any) => {

					if (!traitName.includes('#')) {
						setError(`Make sure all files are named correctly with containing a "#", followed by a number`)
					}

					// Do whatever you want to do with the file
					const name = traitName.split('#')[0]
					const rarity = Number(traitName.split('#').pop().split('.')[0])

					batched[traitType].totalRarity = batched[traitType].totalRarity + rarity

					if (batched[traitType].traits.some((trait: any) => trait.name === name)) {
						// Because of how we are using duplicate names and lumping variations of traits into the same trait
						// i.e Base.
						const index = batched[traitType].traits.findIndex((t: any) => t.name === name)
						const entry = batched[traitType].traits[index]

						batched[traitType].traits[index] = {
							name: entry.name,
							value: entry.value + rarity
						}

					} else {
						batched[traitType].traits.push({
							name: `${name}#${rarity}`,
							value: rarity
						})
					}
				})
		
			}

			const percentageBatched = Object.entries(batched).map((traitArr) => {
				const [traitType, values] = traitArr as any
				return {
					[traitType]: values.traits.map((trait: any, index: number) => {
						return {
							[trait.name]: `${((trait.value / values.totalRarity) * 100).toFixed(2)}%`,
						}
					})
				}
		
			})

			setData(percentageBatched)

		})()
	}, [])

	return (
		<>
			<Head>
				<title>Create Next App</title>
				<meta name="description" content="Generated by create next app" />
				<meta name="viewport" content="width=device-width, initial-scale=1" />
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<main className={styles.main}>
				{error ? <h1>{error}</h1> : data.length > 0 && data.map((entry: any) => (
					Object.entries(entry).map(([key, value]) => (
						<div key={key}>
							<h1 className={inter.className}>{key}</h1>
							{/* {Object.entries(value).map((e) => (
								<div className={inter.className}>{JSON.stringify(value)}</div>
							))} */}
							{(value as any).map((e: any) => {
								const [heck, value] = Object.entries(e)[0] as any
								return (
									<div>
										<span>{heck} - </span>
										<span>{value}</span>
									</div>
								)
							})}
						</div>
					))
				))}
			</main>
		</>
	)
}

